    public void tampilDataSiswa(String where) {
          try {
              st = cn.createStatement();
              tabModelSiswa.getDataVector().removeAllElements();
              tabModelSiswa.fireTableDataChanged();
              rs = st.executeQuery("SELECT * FROM siswa " + where);
              
          while (rs.next()) {
              Object[] data = {
              rs.getString("id"),
              rs.getString("nis"),
              rs.getString("nama"),
              rs.getString("id_kelas"),
              rs.getString("alamat"),
              rs.getString("no_telp"),
              rs.getString("id_spp"),
          };
          
            tabModelSiswa.addRow(data);
          } 
        } catch(Exception e) {
         e.printStackTrace();
        }
      }
    

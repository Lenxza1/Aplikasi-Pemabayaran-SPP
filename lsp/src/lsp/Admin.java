    public void tampilDataPetugas(String where) {
          try {
              st = cn.createStatement();
              tabModelPetugas.getDataVector().removeAllElements();
              tabModelPetugas.fireTableDataChanged();
              rs = st.executeQuery("SELECT * FROM petugas " + where);
              
          while (rs.next()) {
              Object[] data = {
              rs.getString("id"),
              rs.getString("username"),
              rs.getString("nama"),
              rs.getString("level"),
          };
          
            tabModelPetugas.addRow(data);
          } 
        } catch(Exception e) {
         e.printStackTrace();
        }
    }
    
